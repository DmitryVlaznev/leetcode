class Solution:
    def get_pivot(self, nums: List[int], l, r) -> int:
        if r - l < 2:
            return l if nums[l] <= nums[r] else r
        mid = l + (r - l) // 2

        if nums[mid] == nums[l] and nums[mid] == nums[r]:
            lp = self.get_pivot(nums, l, mid)
            rp = self.get_pivot(nums, mid, r)

            if nums[lp] != nums[rp]:
                return lp if nums[lp] < nums[rp] else rp
            else:
                return rp if nums[rp] < nums[rp - 1] else lp
        elif nums[mid] >= nums[l] and nums[mid] <= nums[r]:
            return self.get_pivot(nums, l, mid)
        elif nums[mid] > nums[r]:
            return self.get_pivot(nums, mid, r)
        else:
            return self.get_pivot(nums, l, mid)

    def search(self, nums: List[int], target: int) -> bool:
        if len(nums) < 2:
            return False if not nums else nums[0] == target
        pivot = self.get_pivot(nums, 0, len(nums) - 1)

        l, r, ll = -1, len(nums), len(nums)
        while r - l > 1:
            mid = l + (r - l) // 2
            if nums[(mid + pivot) % ll] >= target:
                r = mid
            else:
                l = mid
        if r == len(nums):
            return False
        return nums[(r + pivot) % ll] == target


